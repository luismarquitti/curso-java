public class TestaFor {

    public static void main(String[] args) {

        for (int contador = 0; contador <= 10; contador++) {
            
            // O primeiro argumento do for é o 'início', o segundo é a condição e o terceiro o que ele faz após executar o código entre as chaves do for.
            
            System.out.println(contador);
        }

    }
}